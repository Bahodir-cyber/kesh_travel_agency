// Function to apply translations to the page
function applyTranslations(lang) {
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (element.hasAttribute('data-html')) {
      element.innerHTML = translations[lang][key];
    } else {
      element.textContent = translations[lang][key];
    }
  });

  // Update form labels and placeholders
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    element.setAttribute('placeholder', translations[lang][key]);
  });

  // Update select options
  const select = document.getElementById('select');
  if (select) {
    const options = [
      { value: "", text: translations[lang].feedback },
      { value: "", text: translations[lang].request },
      { value: "", text: translations[lang].complain }
    ];
    select.innerHTML = options.map(opt => `<option value="${opt.value}">${opt.text}</option>`).join('');
  }
}

// Function to change language
function changeLanguage(lang) {
  localStorage.setItem('selectedLanguage', lang);
  applyTranslations(lang);
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', () => {
  const savedLanguage = localStorage.getItem('selectedLanguage') || 'en';
  applyTranslations(savedLanguage);
});